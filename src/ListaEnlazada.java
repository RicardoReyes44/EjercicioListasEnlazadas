
/*
 * OPERACIONES BASICAS DE LAS LISTAS ENLAZADAS
 * 1) Creacion
 * 2) Agregar elemento
 *     2a) Al inicio
 *     2b) Al final
 *     2c) En un lugar especifico
 *     
 * 3) Eliminar elemento
 *     3a) Al inicio
 *     3b) Al final
 *     3c) En un lugar especifico
 * 
 * 4) Recorrer la lista(mostrarElemento)
 * 5) Buscar elemento
 * 6) Vaciar lista
 * 7) Mostrar cantidad de elementos
 * 8) Mostrar primero
 * 9) Mostrar ultimo
 * 10) Verificar NO vacia
 * */

public class ListaEnlazada {

	private Nodo primero;
	private Nodo ultimo;
	
	//1) Creacion
	public ListaEnlazada() {
		primero = ultimo = null;
	}
	
	private void primeroApuntaA(Nodo apuntar) {
		primero = apuntar;
	}
	
	private void ultimoApuntaA(Nodo apuntar) {
		ultimo = apuntar;
	}
	
	//2) Agregar elemento
	

}
